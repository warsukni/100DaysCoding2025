public class Warsukn3 {

    public static void main(String[] args) {
        //penggunaan println
        // println digunakan untuk mencetak teks lalu berpindah ke baris baru
        System.out.println("Nama: warsukni");
        System.out.println("Umur: 18 tahun");
        System.out.println("Prodi: Informatika");
        //penggunaan print
        //print digunakan untuk mencetak teks tanpa berpindah ke baris baru
        System.out.print("Hallo!! nama saya warsukni, ");
        System.out.print("Umur 18 tahun kelas A Informatika");
        //Penggunaan printf
        //printf digunakan untuk mencetak teks dengan format tertentu
        //%d untuk angka (integer)
        //%s untuk string
        System.out.printf("Nama:%s %nUmur:%d %nProdi%s", "warsukni",18,"Informatika");
        System.out.println("");
 }
 }
    

    
